## 3. Performance im Live-Interview (Modul 6)

### 3.1 Energie & Präsenz
- **Status zeigen, nicht Status behaupten.** Aufrechte Haltung, offene Gestik, ruhiges Tempo. Nervosität zeigt sich zuerst in zu schnellem Sprechen – bewusst 10–15% langsamer sprechen als im Alltag.
- **Pausen sind Stärke, keine Schwäche.** Eine kurze Pause vor einer wichtigen Aussage erzeugt Aufmerksamkeit und wirkt souverän. Die Angst vor „Sendepausen" führt zu Füllwörtern – lieber bewusst schweigen.
- **Lächeln und Energie auch bei ernsten Themen** in der Anmoderation, dann bewusst Ernsthaftigkeit zeigen, wenn es inhaltlich passt. Monotonie ist der größte Feind von Aufmerksamkeit in Video-Formaten.

### 3.2 Antwortstruktur: Die PREP-Methode
Bewährtes Werkzeug aus dem klassischen Medientraining, gut geeignet für Live-Calls mit Zeitdruck:
- **P**oint – Kernaussage zuerst, in einem Satz.
- **R**eason – kurze Begründung.
- **E**xample – Beleg, Geschichte oder Zahl.
- **P**oint – Kernaussage wiederholen, ggf. mit Handlungsaufforderung.

So bleibt jede Antwort auch dann verständlich, wenn sie unterbrochen wird oder die Zuschauer erst später einsteigen.

### 3.3 Umgang mit kontroversen/schwierigen Fragen
Gerade im Freigeist-Kontext (Themen abseits des Mainstreams) ist das kein Nebenschauplatz, sondern zentral:
- **Nie defensiv werden.** Angriffslustige oder skeptische Fragen sind in einem „unzensierten" Format Teil des Deals – sie souverän und ruhig zu beantworten, stärkt die Glaubwürdigkeit mehr als sie zu umgehen.
- **Unterscheiden zwischen Meinung, Erfahrung und Fakt** – und das auch sprachlich kennzeichnen („Meine Erfahrung zeigt…" vs. „Studien zeigen…"). Das schützt Glaubwürdigkeit UND rechtlich vor Fehlinterpretation als absolute Tatsachenbehauptung.
- **Bridging statt Ausweichen**: Nicht ignorieren, sondern kurz anerkennen („Das ist eine berechtigte Frage…") und dann gezielt zur eigenen Kernbotschaft überleiten.
- **Nie über Dritte in einer Weise sprechen, die nicht auch vor deren Gesicht gesagt würde** – bei einem dauerhaft archivierten Interview ein wichtiger Selbstschutz.

### 3.4 Sprache: Klartext statt Worthülsen
Die Zielgruppe erkennt PR-Floskeln sofort und bestraft sie mit Vertrauensverlust. Konkret bedeutet das:
- Aktive Sprache statt Passiv-Konstruktionen.
- Konkrete Beispiele statt abstrakter Prinzipien.
- Eigene Haltung klar benennen, auch wenn sie polarisiert – Unverbindlichkeit wirkt in diesem Format unglaubwürdiger als eine klare, sachlich begründete Position.

### 3.5 Blickkontakt & Kamera-Präsenz
Bei Video-Calls: Blick regelmäßig in die Kamera (nicht nur auf den Bildschirm mit dem Gesprächspartner) richten, wenn eine zentrale Aussage direkt an die Zuschauer geht – das erzeugt das Gefühl von direkter Ansprache, wichtig bei „auf Augenhöhe"-Formaten.

