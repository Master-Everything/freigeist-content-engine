## 5. Die häufigsten Fehler (und wie man sie vermeidet)

| Fehler | Wirkung | Gegenmittel |
|---|---|---|
| Zu viele Fachbegriffe ohne Erklärung | Community fühlt sich ausgeschlossen | Ein-Satz-Test (2.2) konsequent anwenden |
| Skript ablesen / auswendig gelernt klingen | Wirkt unauthentisch, widerspricht „auf Augenhöhe" | Stichwort-Struktur statt Skript |
| Zu lange Antworten ohne klaren Anfang/Ende | Zuschauer steigen aus, F&A-Zeit wird knapp | PREP-Methode (3.2) |
| Defensive Reaktion auf kritische Fragen | Wirkt unsicher, schadet Glaubwürdigkeit | Bridging statt Ausweichen (3.3) |
| Tatsachenbehauptung statt gekennzeichneter Meinung | Rechtliches Risiko, da dauerhaft archiviert | Sprachliche Kennzeichnung (3.3) |
| Monotone Stimme, kein Pausensetzen | Wirkt uninteressant im Video-Format | Tempo & Pausen bewusst einsetzen (3.1) |
| Keine konkrete Handlungsaufforderung am Ende | Community weiß nicht, was als Nächstes zu tun ist | Frage 3 aus Kapitel 1 vorab klären |

