## 2. Vorbereitung / Vorgespräch (Modul 5)

Das Vorgespräch ist der wichtigste Hebel für ein gutes Interview – hier wird die Qualität zu 80% entschieden, nicht während der Aufnahme selbst.

### 2.1 Struktur statt Skript
- **Kein Wort-für-Wort-Skript.** Skripte klingen vorgelesen und widersprechen der „auf Augenhöhe"-Positionierung. Stattdessen: 3–5 Kernbotschaften + dazugehörige Geschichten/Belege, in Stichworten.
- **Bridging-Sätze vorbereiten**: kurze, geübte Formulierungen, mit denen man von einer schwierigen oder unpassenden Frage zur eigenen Kernbotschaft zurückfindet (z. B. „Die eigentliche Frage dahinter ist doch…").
- **Die W-Fragen des Gastgebers antizipieren**: Auf Basis des Themenkatalogs aus dem Vorgespräch (Hauptfragen/Vertiefungsfragen) die wahrscheinlichen Nachfragen durchspielen – gerade bei kontroversen Themen kommt die Anschlussfrage fast immer.

### 2.2 Der Ein-Satz-Test
Jede Kernaussage sollte in einem Satz ohne Fachjargon verständlich sein. Wenn ein Laie den Satz nicht wiederholen könnte, ist er zu kompliziert für ein Live-Format.

### 2.3 Die „Drei Anekdoten"-Regel
Für jedes Thema mindestens eine konkrete, persönliche Geschichte vorbereiten. Fakten überzeugen den Kopf, Geschichten bleiben im Gedächtnis und werden geteilt – das ist bei einem Community-Format mit Weiterverbreitungscharakter besonders wichtig.

### 2.4 Rote Linien klären
Beim Vorgespräch aktiv ansprechen: Gibt es Themen, Formulierungen oder Behauptungen, die bewusst vermieden werden sollen (z. B. konkrete Heilversprechen, Anlageempfehlungen, Aussagen mit rechtlichem Risiko)? Das ist kein Zeichen von Zurückhaltung, sondern professionelle Selbstverantwortung – gerade weil Inhalte langfristig im Archiv bleiben.

### 2.5 Technik-Check
Kamera auf Augenhöhe, gutes Licht (Lichtquelle vor dem Gesicht, nicht dahinter), stabiles Internet, externes Mikrofon falls möglich, ruhiger Hintergrund ohne Ablenkung. Bei einem Live-Format gibt es keine zweite Chance für einen Technik-Fail.

