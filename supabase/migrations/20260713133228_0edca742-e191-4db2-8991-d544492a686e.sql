## 4. Die Live-F&A-Runde danach

Diese Phase wird in der Vorbereitung oft unterschätzt, ist aber beim Freigeist-Format ein eigener Programmpunkt:

- **Antworten kürzer halten** als im Hauptinterview – F&A lebt von Tempo und mehreren beantworteten Fragen, nicht von langen Monologen.
- **Ungeplante/unbequeme Fragen aus der Community sind wahrscheinlich** – hier zahlt sich die Vorbereitung der roten Linien (Punkt 2.4) direkt aus.
- **Bei Unsicherheit lieber ehrlich „Das kann ich nicht seriös beantworten, aber…" sagen** als zu improvisieren – Ehrlichkeit über Wissensgrenzen stärkt die Autorität in einer Community, die genau auf Authentizität wert legt.

