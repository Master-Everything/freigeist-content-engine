## 0. Das Format verstehen, bevor man coacht

Jeder gute Medientrainer coacht auf das *tatsächliche* Format hin, nicht auf ein generisches Interview. Das Freigeist-Format hat einige Besonderheiten, die die Tipps unten prägen:

- **Live Calls, jeden Donnerstag 19:00 Uhr**, mit anschließender **interaktiver Live-F&A** – der Speaker steht nicht nur der Moderation, sondern danach auch direkt der Community Rede und Antwort.
- **Positionierung „unzensiert, unabhängig, unverblümt"** und „auf Augenhöhe" – die Zielgruppe erwartet explizit *keine* geglättete PR-Sprache, sondern Klartext und persönliche Haltung.
- **Themenspektrum**: Bewusstsein, Gesundheit, Frequenzmedizin, Recht/Souveränität, Finanzen, alternative Medien – oft Themen abseits des Mainstream-Konsenses.
- **Community-Charakter**: Die Zuschauer sind wiederkehrende Mitglieder eines geschlossenen Netzwerks, kein anonymes Massenpublikum. Vertrauen und Wiedererkennbarkeit zählen mehr als einmalige Schlagzeilen-Momente.
- **Aufzeichnung mit Nachnutzung**: Die Calls landen im Experten-Archiv und werden später als Profile/Content weiterverwendet (M7/M8) – jeder Versprecher lebt potenziell lange weiter.

Diese fünf Punkte sind der rote Faden für die folgenden Tipps.

